package git.io.join.adapter.out.token;

import jakarta.persistence.*;

@Entity
public class TokenEnitty {
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    @Column(name = "id", nullable = false)
    private Long id;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }
}
